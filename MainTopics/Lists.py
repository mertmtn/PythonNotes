numberList=[1,2,3,4,5,6,7,8,9]

print(numberList[-1]) #It returns last Index of the list

print(numberList[-3])

print(numberList[0]) #It returns first index of the list

#Slicing
print(numberList[:3]) #It returns the element from third index of the list (not included third index)

print(numberList[3:]) #It returns the element from third index of the list (included third index)

print(numberList[2:5])
